package _4_com.RitikCoder;

public class WhileLoop_ICI_inJava {
    public static void main(String[] args) {
        System.out.println("While Loop ICI in Java");
        //Print "5" Natural number using while loop.
        int i= 1;
        while(i<= 5){
            System.out.println(i);
            i++;
        };
    }
}
