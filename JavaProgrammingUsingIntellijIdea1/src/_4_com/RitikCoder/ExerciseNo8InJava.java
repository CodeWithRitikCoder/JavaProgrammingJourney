//Write a program to print the element of an Array in reverse order using Decrementing for loop.
package _4_com.RitikCoder;

public class ExerciseNo8InJava {
    public static void main(String[] args) {
        System.out.println("Exercise No8 In Java");

        int []arr= {23, 31, 22, 5, 67};
        for(int i= 4; i>= 0; i--){
            System.out.println(arr[i]);
        }
    }
}
