package _4_com.RitikCoder;

public class ForLoop_ICI_inJava {
    public static void main(String[] args) {
        System.out.println("For Loop ICI in Java");

        //Print Factorial of "6" using for loop.
        int factorial= 1;
        for(int i= 1; i<= 6; i++){
            factorial= factorial* i;
        }
        System.out.println("Factorial of 6 :- "+ factorial);
    }
}
