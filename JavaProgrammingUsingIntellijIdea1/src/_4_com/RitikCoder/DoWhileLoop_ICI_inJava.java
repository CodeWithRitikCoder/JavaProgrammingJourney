package _4_com.RitikCoder;

public class DoWhileLoop_ICI_inJava {
    public static void main(String[] args) {
        System.out.println("Do while Loop ICI in Java");

        //Print first "5" Even number using do while loop.
        int i= 1;
        do{
            System.out.println(i* 2);
            i++;
        }while(i<= 5);
    }
}