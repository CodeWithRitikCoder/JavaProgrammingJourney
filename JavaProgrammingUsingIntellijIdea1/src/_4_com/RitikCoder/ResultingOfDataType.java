package _4_com.RitikCoder;

public class ResultingOfDataType {
    public static void main(String [] arguments){
        char a= 'A';
        char b= 'a';
        System.out.println(+ a);//This will print Sky value of (a).
        System.out.println(+ b);//This will print Sky value of (A).
        System.out.println(a);//This will print Char value of (a).
        System.out.println(b);//This will print char value of (A).
        System.out.println(++a);//This will print char value of (a) after increment.
        System.out.println(++b);//This will print char value of (A) after increment.
    }
}
