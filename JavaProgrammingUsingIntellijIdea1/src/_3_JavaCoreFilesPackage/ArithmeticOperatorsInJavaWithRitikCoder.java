package _3_JavaCoreFilesPackage;

public class ArithmeticOperatorsInJavaWithRitikCoder {
    public static void main(String[] args) {
        System.out.println("\tThese all are Arithmetic operators in Java.");
        int a= 5, b= 2;
        System.out.println("Addition of A+B is = "+ (a+ b));
        System.out.println("Subtraction of A-B is = "+ (a- b));
        System.out.println("Multiplication of A*B is = "+ (a* b));
        System.out.println("Division of A/B is = "+ (a/ b));
        System.out.println("Modulus of A%B is = "+ (a% b));
        System.out.println("Increment of A%B is = "+ a++);
        System.out.println("Now the value of A is = "+ a);
        System.out.println("Decrement of A%B is = "+ b--);
        System.out.println("Now the value of B is = "+ a);
    }
}
