package _3_JavaCoreFilesPackage;

//Code a software to use of Increment and decrement operators in Java with RitikCoder.
public class IncrementAndDecrementOperatorInJavaWithRitikCoder {
    public static void main(String[] args) {
        System.out.println("Use of Increment and Decrement Operators in Java.");
        int a= 5;
        System.out.println("Value of A is = "+ a);
        System.out.println("Value of A after pre-increment is = "+ ++a);
        System.out.println("Value of A after post-increment is = "+ a++);
        System.out.println("Value of A after post-decrement is = "+ a--);
        System.out.println("Value of A after pre-decrement is = "+ --a);
    }
}