package _3_JavaCoreFilesPackage;

//Code a software to use of all Assignment operators in Java With RitikCoder.
public class AssignmentOperatorsInJavaWithRitikCoder {
    public static void main(String[] args) {
        System.out.println("\tThese all are Assignment operators in Java.\n");
        int a= 2, b= 4;
        char z= 'C';
        float pi= 3.144f;
        System.out.println("The value of PI is = "+ pi);
        System.out.println("The value of z is = "+ z);
        System.out.println("The value of a = "+ a+ " and b = "+ b);
        System.out.println("If A= B than A is = "+ (a= b));
        System.out.println("If A+= B than A is = "+ (a+= b));
        System.out.println("If A-= B than A is = "+ (a-= b));
        System.out.println("If A*= B than A is = "+ (a*= b));
        System.out.println("If A/= B than A is = "+ (a/= b));
        System.out.println("If A%= B than A is = "+ (a%= b));
    }
}