package _3_JavaCoreFilesPackage;

//Code a software to use all Bitwise operators in java with RitikCoder.
public class BitwiseOperatorsInJavaWithRitikCoder {
    public static void main(String[] args) {
        System.out.println("These all are Bitwise operators in java.\n");
        int a= 1, b= 0;
        System.out.println("A & B is = "+ (a& b));
        System.out.println("A | B is = "+ (a| b));
        System.out.println("A ^ B is = "+ (a^ b));
        System.out.println((a< b));
    }
}